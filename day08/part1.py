from __future__ import annotations

import argparse
import os

import pytest

INPUT_TXT = os.path.join(os.path.dirname(__file__), "input.txt")


displays = {0: 6, 1: 2, 2: 5, 3: 5, 4: 4, 5: 5, 6: 6, 7: 3, 8: 7, 9: 6}
options = {k: [] for k in range(2, 8)}
for k in range(2, 8):
    for digit, size in displays.items():
        if size == k:
            options[k].append(digit)


def compute(input: str) -> int:

    xs = input.splitlines()

    n = 0
    for x in xs:
        rs = x.split("|")[1].split()
        for r in rs:
            if len(options[len(r)]) == 1:
                n += 1

    return n


